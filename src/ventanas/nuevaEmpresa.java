    private void btncrearEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearEmpresaActionPerformed
        // TODO add your handling code here:
        int validacion = 0;
        String empresa, idempresa;

        idempresa = txt_idempresa.getText().trim();
        empresa = txt_nombreEmpresa.getText().trim();

        if (idempresa.equals("")) {
            validacion++;
        }
        if (empresa.equals("")) {
            validacion++;
        }

        try {
            Connection cn = conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select id_empresa from empresa where id_empresa = '" + idempresa + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "ID de empresa NO disponible");
                limpiar();
                cn.close();
            } else {
                cn.close();
                if (validacion == 0) {
                    try {
                        Connection cn2 = conexion.conectar();
                        PreparedStatement pst2 = cn2.prepareStatement(
                                "insert into empresa values (?,?)");
                        pst2.setString(1,idempresa);
                        pst2.setString(2, empresa);
                        pst2.executeUpdate();
                        cn2.close();
                        limpiar();
                        
                        JOptionPane.showMessageDialog(null, "Registro exitoso");
                        this.dispose();
                        
                    } catch (Exception e) {
                        System.err.println("Error en registrar usuario"+e);
                        JOptionPane.showMessageDialog(null, "Error al registrar"+e);
                        limpiar();
                        
                    }
                } else {
                        JOptionPane.showMessageDialog(null, "Â¡Debes de llenar todos los campos!");
                }

            }
        } catch (Exception e) {
            System.err.println("Error en validar nombre de usuario" + e);
            JOptionPane.showMessageDialog(null, "Error al comparar id de empresa" + e);
        }
    }//GEN-LAST:event_btncrearEmpresaActionPerformed

